/**
 * Un objet ayant une position en deux dimentions
 *
 * @author Litarvan
 * @since 1.0.0
 */
public interface Positionable2D
{
    /**
     * @return La position X de l'objet
     */
    int getX();

    /**
     * @return La position Y de l'objet
     */
    int getY();
}
